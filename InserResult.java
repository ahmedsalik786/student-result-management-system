    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String rollno=jTextField1.getText();
        String EM=jTextField2.getText();
        String DBMS=jTextField3.getText();
        String OS=jTextField4.getText();
        String Maths=jTextField5.getText();
        String ML=jTextField6.getText();
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","s&sgroup");
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from student where rollno='"+rollno+"'");
            if(rs.next()){
                st.executeUpdate("insert into result values('"+rollno+"','"+EM+"','"+DBMS+"','"+OS+"','"+Maths+"','"+ML+"')");
                JOptionPane.showMessageDialog(null, "Data updated sucessfully");
                setVisible(false);
                new InserResult().setVisible(true);
                
            }else{
                JOptionPane.showMessageDialog(null, "Roll number does not exist");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton6ActionPerformed

